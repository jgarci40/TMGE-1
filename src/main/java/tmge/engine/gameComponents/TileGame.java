package tmge.engine.gameComponents;

public class TileGame {
	int rows;
	int columns;

	public TileGame(int rows, int columns){
		this.rows = rows;
		this.columns = columns;
	}

	public String toString() {
		return "";
	}

	public int getRows() {
		return rows;
	}

	public int getColumns() {
		return columns;
	}
}
